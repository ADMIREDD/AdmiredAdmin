CREATE DATABASE IF NOT EXISTS base_proyecto;
USE base_proyecto;

CREATE TABLE areas_comunes (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    TIPO_AREA_ID INT(11) NOT NULL,
    DESCRIPCION_AREA VARCHAR(45) NOT NULL,
    HORARIO_USO VARCHAR(45) NOT NULL,
    REGLAS_NORMAS VARCHAR(45) NOT NULL,
    VALOR INT(11) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE cuotas_administracion (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    FECHA_MES VARCHAR(45) NOT NULL,
    ESTADO VARCHAR(45) NOT NULL,
    VALOR VARCHAR(45) NOT NULL,
    NO_APTO INT(11) NOT NULL,
    FECHA_PAGO DATE NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE documento (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    TIPO VARCHAR(45) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE estados (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    TIPO VARCHAR(45) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE pqr (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    DETALLE VARCHAR(300) NOT NULL,
    ESTADO_ID INT(11) NOT NULL,
    USUARIO_ID INT(11) NOT NULL,
    PQR_TIPO INT(11) NOT NULL,
    FECHA_SOLICITUD DATE NOT NULL,
    FECHA_RESPUESTA DATE NOT NULL,
    RESPUESTA VARCHAR(30) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE pqr_tipo (
    ID INT(10) NOT NULL AUTO_INCREMENT,
    NOMBRE VARCHAR(20) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE reservas (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    FECHA_RESERVA DATE NOT NULL,
    ID_ZONA_COMUN INT(11) NOT NULL,
    ESTADO_RESERVA VARCHAR(30) NOT NULL,
    ID_USUARIO INT(11) NOT NULL,
    OBSERVACION_ENTREGA VARCHAR(700) NOT NULL,
    OBSERVACION_RECIBE VARCHAR(700) NOT NULL,
    VALOR INT(11) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE residentes (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    NO_TORRE VARCHAR(45) NOT NULL,
    NO_APARTAMENTO VARCHAR(45) NOT NULL,
    NOMBRE_PROPIETARIO VARCHAR(45) NOT NULL,
    ESTADO VARCHAR(45) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE role (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    TIPO VARCHAR(20) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE tipo_area_comun (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    TIPO VARCHAR(45) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE unidades_residenciales (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    NO_TORRE VARCHAR(45) NOT NULL,
    NO_APARTAMENTO VARCHAR(45) NOT NULL,
    ID_PROPIETARIO INT(11) NOT NULL,
    VALOR_CUOTA VARCHAR(20) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE usuarios (
    ID INT(11) NOT NULL AUTO_INCREMENT,
    NOMBRE VARCHAR(45) NOT NULL,
    APELLIDO VARCHAR(45) NOT NULL,
    TIPO_DOCUMENTO_ID INT(11) NOT NULL,
    NO_DOCUMENTO VARCHAR(45) NOT NULL UNIQUE,
    FECHA_NACIMIENTO VARCHAR(45) NOT NULL,
    EMAIL VARCHAR(45) NOT NULL UNIQUE,
    CONTRASENA VARCHAR(255) NOT NULL,
    TELEFONO INT(11) NOT NULL,
    ROL_ID INT(11) NOT NULL,
    TORRE INT(11) NOT NULL,
    APTO INT(11) NOT NULL,
    RESIDENTES_ID INT(11),
    CUOTAS_ADMIN_ID INT(11),
    UNIDAD_RESIDENCIAL_ID INT(11),
    AREA_COMUN_ID INT(11),
    PRIMARY KEY (ID)
);
